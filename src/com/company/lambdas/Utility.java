package com.company.lambdas;


public class Utility {

    static int add(int a,int b){
        return a+b;
    }

    static int sub(int a,int b){
        return a-b;
    }

    static int multi(int a,int b){
        return a*b;
    }

    static int divide(int a,int b){
        return a/b;
    }
}
